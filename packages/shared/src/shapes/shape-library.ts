/** 按类别获取形状 */
export function getShapesByCategory(category: ShapeCategory): ShapeEntry[] {
  return ALL_SHAPES.filter(s => s.category === category);
}

/** 按 ID 获取形状 */
export function getShapeById(id: string): ShapeEntry | undefined {
  return ALL_SHAPES.find(s => s.id === id);
}

/** 获取所有分类 */
export function getShapeCategories(): Array<{ category: ShapeCategory; name: string; nameEn: string; icon: string; count: number }> {
  return Object.entries(SHAPE_CATEGORY_META).map(([key, value]) => ({
    category: key as ShapeCategory,
    ...value,
  }));
}

